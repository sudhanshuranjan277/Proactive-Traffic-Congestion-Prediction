# Console Summary
# ======================================

def print_comparison(
    comparison_rows,
):
    """
    Prints a formatted comparison table
    showing the performance of both
    traffic controllers.
    """

    print(
        "\n"
        + "=" * 100
    )

    print(
        "Fixed-Time vs Proactive "
        "LSTM + DDQN Comparison"
    )

    print(
        "=" * 100
    )

    print(
        f"{'Metric':<28}"
        f"{'Fixed':>14}"
        f"{'Proactive':>14}"
        f"{'Improvement':>16}"
        f"{'Result':>16}"
    )

    print(
        "-" * 100
    )

    for row in comparison_rows:

        improvement = row[
            "improvement_percent"
        ]

        if improvement is None:

            improvement_text = "N/A"

        else:

            improvement_text = (
                f"{improvement:.2f}%"
            )

        print(
            f"{row['metric_label']:<28}"
            f"{row['fixed_value']:>14.6f}"
            f"{row['proactive_value']:>14.6f}"
            f"{improvement_text:>16}"
            f"{row['result']:>16}"
        )

    print(
        "=" * 100
    )


def print_result_summary(
    comparison_rows,
):
    """
    Prints an overall summary showing the
    number of improved, degraded and
    unchanged traffic performance metrics.
    """

    improved_count = sum(
        row["result"] == "IMPROVED"
        for row in comparison_rows
    )
    degraded_count = sum(
        row["result"] == "DEGRADED"
        for row in comparison_rows
    )
    unchanged_count = sum(
        row["result"] == "UNCHANGED"
        for row in comparison_rows
    )
    print(
        "\nComparison Summary"
    )
    print(
        "-" * 60
    )
    print(
        f"Improved Metrics  : "
        f"{improved_count}"
    )
    print(
        f"Degraded Metrics  : "
        f"{degraded_count}"
    )
    print(
        f"Unchanged Metrics : "
        f"{unchanged_count}"
    )
    print(
        f"Total Metrics     : "
        f"{len(comparison_rows)}"
    )
