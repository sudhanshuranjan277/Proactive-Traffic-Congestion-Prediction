METRIC_DIRECTIONS = {
    "average_queue_length": "lower",
    "average_waiting_time": "lower",
    "average_speed": "higher",
    "average_travel_time": "lower",
    "total_throughput": "higher",
    "congestion_ratio": "lower",
    "spillback_events": "lower",
}


METRIC_LABELS = {
    "average_queue_length": (
        "Average Queue Length"
    ),
    "average_waiting_time": (
        "Average Waiting Time"
    ),
    "average_speed": (
        "Average Speed"
    ),
    "average_travel_time": (
        "Average Travel Time"
    ),
    "total_throughput": (
        "Total Throughput"
    ),
    "congestion_ratio": (
        "Congestion Ratio"
    ),
    "spillback_events": (
        "Spillback Events"
    ),
}


# Metric Loading

def load_metrics(file_path):
    """
    Loads evaluation metrics generated by
    evaluate_controller.py.

    The CSV must contain exactly one row with all
    required traffic performance metrics.
    """

    if not os.path.exists(
        file_path
    ):
        raise FileNotFoundError(
            "Metric file not found: "
            f"{file_path}"
        )

    with open(
        file_path,
        "r",
        encoding="utf-8",
        newline="",
    ) as csv_file:

        reader = csv.DictReader(
            csv_file
        )

        rows = list(
            reader
        )

    if len(rows) != 1:
        raise ValueError(
            "Metric file must contain exactly "
            "one metric row: "
            f"{file_path}"
        )

    raw_metrics = rows[0]

    missing_metrics = (
        set(METRIC_DIRECTIONS)
        - set(raw_metrics)
    )

    if missing_metrics:
        raise ValueError(
            "Metric file is missing required "
            "metrics: "
            f"{sorted(missing_metrics)}"
        )

    metrics = {}

    for metric_name in (
        METRIC_DIRECTIONS
    ):

        try:

            metric_value = float(
                raw_metrics[
                    metric_name
                ]
            )

        except (
            TypeError,
            ValueError,
        ) as error:

            raise ValueError(
                "Invalid numeric value for "
                f"metric '{metric_name}' "
                f"in file: {file_path}"
            ) from error

        if not math.isfinite(
            metric_value
        ):
            raise ValueError(
                "Non-finite value for "
                f"metric '{metric_name}' "
                f"in file: {file_path}"
            )

        metrics[
            metric_name
        ] = metric_value

    return metrics
# Improvement Calculation

def calculate_improvement(
    fixed_value,
    proactive_value,
    direction,
):
    """
    Calculates the percentage improvement of the
    proposed controller relative to the baseline.

    Lower-is-better metrics:
    ((Fixed - Proposed)/Fixed)*100

    Higher-is-better metrics:
    ((Proposed - Fixed)/Fixed)*100
    """

    if fixed_value == 0.0:

        return None

    if direction == "lower":

        improvement = (
            (
                fixed_value
                - proactive_value
            )
            / abs(
                fixed_value
            )
            * 100.0
        )

    elif direction == "higher":

        improvement = (
            (
                proactive_value
                - fixed_value
            )
            / abs(
                fixed_value
            )
            * 100.0
        )

    else:

        raise ValueError(
            "Unsupported metric direction: "
            f"{direction}"
        )

    return float(
        improvement
    )
def determine_result(
    fixed_value,
    proactive_value,
    direction,
):
    """
    Determines whether the proposed controller
    performed better, worse, or equal compared
    to the baseline controller.

    Returns:
    IMPROVED
    DEGRADED
    UNCHANGED
    """
    if math.isclose(
        fixed_value,
        proactive_value,
        rel_tol=1e-9,
        abs_tol=1e-12,
    ):
        return "UNCHANGED"

    if direction == "lower":

        if proactive_value < fixed_value:
            return "IMPROVED"

        return "DEGRADED"

    if direction == "higher":

        if proactive_value > fixed_value:
            return "IMPROVED"

        return "DEGRADED"

    raise ValueError(
        "Unsupported metric direction: "
        f"{direction}"
    )
# Controller Comparison
def compare_controllers(
    fixed_metrics,
    proactive_metrics,
):
    """
    Compares all evaluation metrics between the
    baseline Fixed-Time controller and the proposed
    LSTM + DDQN controller.

    For every metric this function:
    1. Reads both values.
    2. Calculates percentage improvement.
    3. Determines the comparison result.
    4. Stores the comparison for reporting.
    """
    comparison_rows = []

    for (
        metric_name,
        direction,
    ) in METRIC_DIRECTIONS.items():

        fixed_value = float(
            fixed_metrics[
                metric_name
            ]
        )

        proactive_value = float(
            proactive_metrics[
                metric_name
            ]
        )

        improvement = (
            calculate_improvement(
                fixed_value,
                proactive_value,
                direction,
            )
        )

        result = determine_result(
            fixed_value,
            proactive_value,
            direction,
        )

        comparison_rows.append(
            {
                "metric": metric_name,
                "metric_label": (
                    METRIC_LABELS[
                        metric_name
                    ]
                ),
                "better_direction": (
                    direction
                ),
                "fixed_value": (
                    fixed_value
                ),
                "proactive_value": (
                    proactive_value
                ),
                "improvement_percent": (
                    improvement
                ),
                "result": result,
            }
        )

    return comparison_rows


# ======================================
# Save Comparison
# ======================================

def save_comparison(
    comparison_rows,
    output_path,
):
    """
    Saves the comparison results into
    controller_comparison.csv.

    The exported CSV is used for performance
    analysis and research reporting.
    """

    if not comparison_rows:
        raise ValueError(
            "Controller comparison is empty."
        )

    fieldnames = [
        "metric",
        "metric_label",
        "better_direction",
        "fixed_value",
        "proactive_value",
        "improvement_percent",
        "result",
    ]

    with open(
        output_path,
        "w",
        encoding="utf-8",
        newline="",
    ) as csv_file:

        writer = csv.DictWriter(
            csv_file,
            fieldnames=fieldnames,
        )

        writer.writeheader()

        writer.writerows(
            comparison_rows
        )


